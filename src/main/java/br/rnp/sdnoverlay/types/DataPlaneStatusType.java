package br.rnp.sdnoverlay.types;

public class DataPlaneStatusType {

    private Boolean active;
    private int version;
    private Boolean versionConsistent;

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public Boolean getVersionConsistent() {
        return versionConsistent;
    }

    public void setVersionConsistent(Boolean versionConsistent) {
        this.versionConsistent = versionConsistent;
    }
}
